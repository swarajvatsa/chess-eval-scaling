// searchbench.cpp — Measure the PURE EFFICIENCY of our search machinery, with the
// judge factored out.
//
// The question: how fast is our C++ search *code itself* — movegen, make/unmake,
// move ordering, alpha-beta/PVS, the transposition table — at walking the tree down
// to the leaves, and how does that cost scale with how many pieces are on the board?
//
// To answer it cleanly we must remove the evaluator from the measurement. So we run
// the REAL, UNMODIFIED search, but swap the leaf score for a cheap pseudo-random
// number derived from the position's hash (Judge::dummy). The leaf is then ~free, so
// the time we measure is almost entirely the search machinery.
//
// WHY FIXED TIME, NOT FIXED DEPTH: a random leaf score gives alpha-beta useless
// bounds, so pruning collapses and a fixed-depth search explodes to an unbounded node
// count (it never finishes at depth 8 in the opening). The robust control is the
// opposite: give each position a FIXED TIME BUDGET, then count how much work the
// machinery got through. That always terminates and yields the metric we actually
// want — throughput (nodes/sec, ns/node) — plus "how far can I explore" (depth, nodes)
// in a real move's worth of time.
//
// We run each position twice at the same time budget: once with the DUMMY leaf score
// (pure search machinery) and once with the REAL judge (search + eval). Comparing
// nodes/sec between the two shows how much of a real search's time is the evaluator
// versus the bare tree walk.
//
// Positions are generated by seeded random self-play from the start position (a real
// game naturally sheds pieces through captures, so one game passes through every
// piece count), bucketed by piece count. This avoids junk/composed FENs.
//
// Output: pieces | depth reached | nodes | nodes/sec | ns/node, for dummy and real.

#include "../src/board.h"
#include "../src/judge.h"
#include "../src/search.h"
#include <cstdio>
#include <cstdlib>
#include <cstring>
#include <random>
#include <vector>
#include <map>
#include <algorithm>
#include <chrono>
#include <string>

using namespace chess;

// Count pieces on the board (all squares that aren't empty).
static int piece_count(const Board& b) {
    int n = 0;
    for (int s = 0; s < 64; ++s) if (b.board[s] != NO_PIECE) n++;
    return n;
}

// Generate `per_bucket` real positions for each target piece count by playing seeded
// random games from the start position. Returns {pieces -> [Board,...]}.
static std::map<int, std::vector<Board>>
generate_positions(const std::vector<int>& buckets, int per_bucket, uint64_t seed) {
    std::map<int, std::vector<Board>> out;
    for (int b : buckets) out[b] = {};
    std::mt19937_64 rng(seed);

    auto need_more = [&]() {
        for (int b : buckets) if ((int)out[b].size() < per_bucket) return true;
        return false;
    };

    int games = 0;
    while (need_more() && games < 200000) {
        games++;
        Board b;                       // start position
        for (int ply = 0; ply < 240; ++ply) {
            std::vector<Move> legal;
            b.generate_legal(legal);
            if (legal.empty()) break;  // checkmate or stalemate -> new game
            int pc = piece_count(b);
            auto it = out.find(pc);
            if (it != out.end() && (int)it->second.size() < per_bucket) {
                // Avoid trivially-near-duplicate positions: only sample every few plies.
                if (ply >= 6) it->second.push_back(b);
            }
            const Move& m = legal[rng() % legal.size()];
            b.make_move(m);
        }
    }
    return out;
}

// Run the real search for a fixed TIME budget on one position; return (depth reached,
// nodes, seconds actually elapsed). Fresh TT each call so it's a clean cold
// measurement, single thread.
static void search_for_time(const Judge* judge, Board b, double movetime,
                            int& depth_out, long& nodes_out, double& secs_out) {
    TT tt; tt.resize_mb(256); tt.clear();
    SearchLimits lim;
    lim.max_depth = 64;
    lim.movetime = movetime;   // the stop condition
    lim.max_nodes = 0;
    lim.threads = 1;
    auto t0 = std::chrono::steady_clock::now();
    SearchResult r = think_parallel(judge, &tt, b, lim);
    auto t1 = std::chrono::steady_clock::now();
    depth_out = r.depth;
    nodes_out = r.nodes;
    secs_out = std::chrono::duration<double>(t1 - t0).count();
}

int main(int argc, char** argv) {
    init_attacks();
    std::string qbin = (argc > 1) ? argv[1] : "models/compact_champion.qbin";
    double movetime  = (argc > 2) ? atof(argv[2]) : 1.0;   // seconds per position
    int per_bucket   = (argc > 3) ? atoi(argv[3]) : 12;
    uint64_t seed    = (argc > 4) ? strtoull(argv[4], nullptr, 10) : 20260606ULL;

    std::vector<int> buckets = {32, 28, 24, 20, 16, 12, 10, 8, 6};

    Judge judge;
    if (!judge.load(qbin)) { printf("FAILED to load %s\n", qbin.c_str()); return 1; }

    fprintf(stderr, "generating %d positions/bucket via random self-play (seed %llu)...\n",
            per_bucket, (unsigned long long)seed);
    auto pos = generate_positions(buckets, per_bucket, seed);

    printf("# AMD EPYC 7R13 @ ~3.6 GHz (single core, boosted) | %.2fs/position | %d pos/bucket\n",
           movetime, per_bucket);
    printf("# DUMMY leaf score = PURE SEARCH MACHINERY        ||  REAL judge (search + eval)\n");
    printf("# %-6s %3s | %5s %14s %10s %8s || %5s %14s %10s %8s | %6s\n",
           "pieces", "n",
           "depth", "nodes", "knode/s", "ns/node",
           "depth", "nodes", "knode/s", "ns/node", "eval%");
    fflush(stdout);

    for (int pc : buckets) {
        auto& boards = pos[pc];
        if (boards.empty()) { printf("  %-6d  (no positions found)\n", pc); continue; }
        int navg = (int)boards.size();

        // DUMMY eval: pure search machinery.
        judge.dummy = true;
        long d_nodes = 0; double d_secs = 0; long d_depth = 0;
        for (auto& b : boards) {
            int dep; long n; double s; search_for_time(&judge, b, movetime, dep, n, s);
            d_nodes += n; d_secs += s; d_depth += dep;
        }
        // REAL eval: search + judge.
        judge.dummy = false;
        long r_nodes = 0; double r_secs = 0; long r_depth = 0;
        for (auto& b : boards) {
            int dep; long n; double s; search_for_time(&judge, b, movetime, dep, n, s);
            r_nodes += n; r_secs += s; r_depth += dep;
        }

        double d_knps = d_secs > 0 ? (d_nodes / d_secs) / 1e3 : 0;
        double r_knps = r_secs > 0 ? (r_nodes / r_secs) / 1e3 : 0;
        double d_nspn = d_nodes > 0 ? d_secs * 1e9 / d_nodes : 0;  // ns per node, pure search
        double r_nspn = r_nodes > 0 ? r_secs * 1e9 / r_nodes : 0;  // ns per node, with eval
        // Fraction of a real node's time spent in the evaluator (the rest is machinery).
        double eval_pct = r_nspn > 0 ? 100.0 * (r_nspn - d_nspn) / r_nspn : 0;
        printf("  %-6d %3d | %5.1f %14ld %10.0f %8.1f || %5.1f %14ld %10.0f %8.1f | %5.1f%%\n",
               pc, navg,
               (double)d_depth / navg, d_nodes / navg, d_knps, d_nspn,
               (double)r_depth / navg, r_nodes / navg, r_knps, r_nspn, eval_pct);
        fflush(stdout);
    }
    return 0;
}
